    : Path.GetFullPath(Path.Combine(AppContext.BaseDirectory, "..", "..", "..", "..", ".."));

if (!Directory.Exists(Path.Combine(root, "Areas")))
{
    // Fallback: tim HisMvc.csproj tu thu muc hien tai
    var dir = new DirectoryInfo(AppContext.BaseDirectory);
    while (dir != null && !File.Exists(Path.Combine(dir.FullName, "HisMvc.csproj")))
        dir = dir.Parent;
    if (dir == null)
    {
        Console.Error.WriteLine("Khong tim thay thu muc HisMvc.");
        return 1;
    }
    root = dir.FullName;
}

Console.WriteLine($"Root: {root}");

var patterns = new[] { "*.cshtml", "*.cs" };
var skip = new[] { "\\bin\\", "\\obj\\", "\\Migrations\\", "\\Scripts\\ViConvert\\" };
var utf8 = new UTF8Encoding(encoderShouldEmitUTF8Identifier: false);
var count = 0;

foreach (var pattern in patterns)
{
    foreach (var file in Directory.GetFiles(root, pattern, SearchOption.AllDirectories))
    {
        if (skip.Any(s => file.Contains(s, StringComparison.OrdinalIgnoreCase)))
            continue;

        var text = File.ReadAllText(file, utf8);
        var orig = text;

        foreach (var (from, to) in ordered)
            text = text.Replace(from, to, StringComparison.Ordinal);

        // Gioi tinh badge
        text = text.Replace("> Nu</", "> N\u1EEF</", StringComparison.Ordinal);
        text = text.Replace(">Nu</", ">N\u1EEF</", StringComparison.Ordinal);
        text = text.Replace("> Khac</", "> Kh\u00E1c</", StringComparison.Ordinal);
        text = text.Replace(">Khac</", ">Kh\u00E1c</", StringComparison.Ordinal);
        text = text.Replace("Chua ro</", "Ch\u01B0a r\u00F5</", StringComparison.Ordinal);
        text = text.Replace("\uFFFD", "\u2014", StringComparison.Ordinal);

        if (text != orig)
        {
            File.WriteAllText(file, text, utf8);
            count++;
            Console.WriteLine(Path.GetRelativePath(root, file));
        }
    }
}

Console.WriteLine($"\nDone. {count} file(s) updated.");
return count == 0 ? 0 : 0;
